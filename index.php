Следующим немаловажным фактором является гибридность. То есть возможность совместимости AHD формата с традиционным аналогом.
И ключевая особенность, это стоимость системы видеонаблюдения формата AHD. На данный момент она является вполне бюджетной и соизмерима с традиционными аналоговыми системами.
